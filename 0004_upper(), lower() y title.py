#Los métodos upper(), lower() y title de Python
#Pon en mayúsculas la primera letra de cada palabra de este nombre utilizando uno de los métodos del capítulo:
nom = "jayden hammond caballero".title()
print(nom)
#Deja esta frase totalmente en letras minúsculas utilizando uno de los métodos del capítulo:
frase = "Esta Es Una Frase Para Ser Formateada".lower()
print(frase)
#Deja esta frase totalmente en letras mayúsculas:
may = "Esta Es Una Frase Para Ser Formateada".upper()
print(may)
